#include <stdio.h>
int main()
{

	int a =0;
	int b = 0;
	//논리 연산자 (and,or)이항 연산자 ,(not)단항 연산자

	printf("%d\n", a && b); //and
	printf("%d\n", a || b); //or
	printf("%d\n", !b); //not
	printf("%d\n", !a);
	//	//비교연산자
	printf("%d\n", a == b);
	printf("%d\n", a != b);
	printf("%d\n", a >= b);
	printf("%d\n", a == b);
}


